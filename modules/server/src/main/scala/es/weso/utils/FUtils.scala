package es.weso.utils
import cats.effect._
import cats.arrow.FunctionK
import es.weso.utils.IOUtils._


object FUtils {

  def cnvResource[A, F[_]: Effect](r: Resource[IO,A]):Resource[F,A] = r.mapK(cnv)
 
  def cnv[A,F[_]: Effect]: FunctionK[IO,F] = new FunctionK[IO,F] {
    def apply[A](ioa: IO[A]): F[A] = io2f(ioa)
  }

}